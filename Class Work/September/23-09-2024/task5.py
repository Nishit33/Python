
# Two dictionary connect to each other l is key and l1 is value

l = [1,2,3]         # key 
l1 =[3,4,5]         # value

d={}        # empty dictionary 

for i in range(len(l)):         # for loop i in range len(l) 
    d[l[i]] = l1[i]         # d[l[i]] = l1[i] 


print(l)
print(l1)
print(d)            # print d 


