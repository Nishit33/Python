

# STRING PYTHON ALL METHOD 




s = "Python Programming"



print(len(s))  # length count 


print(s.capitalize())   #first letter capital


print(s.lower())        # All latter lower 


print(s.swapcase())     # small to capital and capital to small 


print(s.center(30,"*"))     # String center 


print(s.count("P"))         # How many "P" in string count 


print(s.endswith("ng"))     # if last "ng" return is true


print(s.find("g",2))    # find letter after 2 number 


print(s.index("g"))     # Same as find letter 


print(s.replace("m","R"))       #replace which letter to another one 


print(s.isalnum())      # All time true showing true but put some space return will be false 


print(s.isalpha())  # Same as isalnum


print(s.title())    # All first letter capital after space is also 


print(s.upper())    # All letter upper case 


