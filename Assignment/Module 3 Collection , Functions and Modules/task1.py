#  How will you remove last object from a list? 
#  Suppose list1 is [2, 33, 222, 14, and 25], what is list1 [-1]? 



list1 = [2,33,222,14,25] 

"""How Will you remove last object from a list ?"""
list1.pop()
print(list1)     # we can use .pop() and remove last elements by default 

"""What is list1 [-1] ? """

print(list1[-1]) # if we use .pop() so answer will be 14 and we use without .pop answer will be 25 



