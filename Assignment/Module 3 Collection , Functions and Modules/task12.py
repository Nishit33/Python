# Write a Python program to convert a list of characters into a string. 

list=input("Enter Character seprated by space : ")      # list input character seprated by space 

char = list.split()             # char = list.split()

string = ""                 # string = ""


for char in char:       # for char in char 
    string += char                  # string += char

print("After convert string is : ",string)          # print after string merge is character to string 





