# Write a Python function to get the largest number, smallest num and sum of all from a list.

l = [32,54,67,31,98,6,12,33]        # list 

largest = max(l)            # largest number 
smallest = min(l)           # smallest 
sum = sum(l)                # sum 


print("Largest : ",largest)         # print all largest , smallest and sum of all list number 
print("Smallest : ",smallest)
print("Total_Sum : ",sum)