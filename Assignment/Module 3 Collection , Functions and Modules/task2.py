# Differentiate between append () and extend () methods? 

"""
--> So differentiate between append and extend 

append ()  --> we can use add a single elements to the end of the list .

extend ()  --> we can use add another list in the list .


"""

l = [1,2,3]         # This is list 

l.append(4)         # append add single elements 
l=[1,2,3,4]         # after append this status


l.extend([5,6,7,8])     # extend list 
l=[1,2,3,4,5,6,7,8]     # after add another list to list 


