# Write a Python program to check a list is empty or not. 

l1=[1,2,3,4,5,6,7]      # l1 is list full of number 

l2=[]           # l2 is empty list 


if len(l1) == 0:        # if len(l1) == 0 then print empty 
    print("Empty")

else:                   # otherwise print not empty 
    print("Not Empty") 



